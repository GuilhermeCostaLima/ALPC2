'''9. Faça um programa para determinar o consumo médio de um automóvel sendo fornecida a distância total percorrida pelo automóvel 
e o total de combustível gasto.'''


distancia = float(input("Distância total (em Km): "))
combustivel = float(input("Combustível gasto (em Litros): "))

consumo_medio = distancia / combustivel

print(f'O consúmo médio é de: {consumo_medio} Km/l')





